											<p>An occupied Palestinian territory Humanitarian Fund success story

Fourteen year-old Dina* is a charming little girl from Gaza who likes sports and drawing. But she was born into extremely challenging family conditions. Her unemployed parents treat her violently. Her mother, who has an untreated psychiatric disability, holds her responsible for feeding, teaching and cleaning her siblings. Any of Dina’s attempts to protect herself from abuse is only encountered with more violence. COVID-19 closures, requiring her to spend more time at home, as well as the May 2021 escalation of hostilities, have only made this worse.

Understandably, Dina’s living conditions have undermined her personal wellbeing: she began wetting the bed and withdrawing from her friends at school. The situation has also undermined her education and, by extension, her prospects for the future. In particular, she has failed Math and Arabic.</p>

<p>In August 2021, supported by the occupied Palestinian territory Humanitarian Fund (oPt HF), NRC launched a project to support education in Gaza, in the aftermath of that year’s escalation. The project enhanced the mental health and psycho-social wellbeing of students, parents and educators, all traumatized by 11 consequent days of armed conflict. Overall, 2,400 students in twenty schools benefitted from recreational activities, extra academic instruction, and psychosocial support.

This was the case with Dina, who took part in the afterschool programme. There, teachers helped her develop learning techniques that work for her, including the use flashcards and worksheets, and a psychological counselor provided support that helped her stop wetting the bed at night.
</p>

<p>As a result, like many other participants, Dina increased her participation in class and became more self-confident. Going to school became something she is looking forward to. “My teachers keep saying I’m smart,” she says. “They complement me for participating in class and in other activities. It just makes me happy.”

While this project focuses on education, other interventions are carried out in parallel to protect children in difficult situations, like Dina’s.</p>
<p>Dina’s teachers say she had enhanced her self-confidence following her participation in the afterschool sessions, has increased her participation in class and is no longer failing basic subjects. “I am very happy that I participated in the lessons with the teachers, Iman and Jasmine, because they helped me pass my exams,” Dina says.</p>
